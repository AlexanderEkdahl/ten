func TestEncoderNameTooLong(t *testing.T) {
	enc := NewEncoder(ioutil.Discard)

	if err := enc.Encode([]int8{}, []int{}, "ü•∞"); err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	if err := enc.Encode([]int8{}, []int{}, "ü•∞üòç"); err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	if err := enc.Encode([]int8{}, []int{}, "ü•∞üòçüé±"); !errors.Is(err, ErrInfoTooLong) {
		t.Fatalf("expected error, got: %v, expected: %v", err, ErrInfoTooLong)
	}
}

func BenchmarkEncoder(b *testing.B) {
	enc := NewEncoder(ioutil.Discard)
	sizes := []int{1e2, 5e2, 1e3, 1e4}

	for _, size := range sizes {
		data := make([]float32, size)
		shape := []int{0}
		b.Run(fmt.Sprintf("%d", size), func(b *testing.B) {
			b.SetBytes(int64(binary.Size(data)))

			for i := 0; i < b.N; i++ {
				enc.Encode(data, shape, "")
			}
		})
	}
}

func TestEncoderDecoder(t *testing.T) {
	tcs := []interface{}{
		[]float32{1, 2, 3},
		[]float64{1, 2, 3},
		[]int8{1, 2, 3},
		[]int16{1, 2, 3},
		[]int32{1, 2, 3},
		[]int64{1, 2, 3},
		[]uint8{1, 2, 3},
		[]uint16{1, 2, 3},
		[]uint32{1, 2, 3},
		[]uint64{1, 2, 3},
	}
	info := "hello"

	for _, tc := range tcs {
		var buf bytes.Buffer
		if err := NewEncoder(&buf).Encode(tc, []int{3}, info); err != nil {
			t.Fatalf("unexpected error: %v", err)
		}

		tensorData, shape, info, err := NewDecoder(&buf).Decode()
		if err != nil {
			t.Fatalf("unexpected error: %v", err)
		}
		if len(shape) != 1 && shape[0] != 3 {
			t.Fatalf("incorrect shape after decoding, got: %d, expected: %d", shape, []int{3})
		}
		if info != "hello" {
			t.Fatalf("incorrect info after decoding, got: %q, expected: %q", info, "hello")
		}
		if !reflect.DeepEqual(tensorData, tc) {
			t.Fatalf("incorrect tensor data after decoding, got: %q, expected: %q", tensorData, tc)
		}
	}
}
