func TestEncoderFloat32(t *testing.T) {
	var b bytes.Buffer
	enc := NewEncoder(&b)

	err := enc.Encode(tensorFloat32.Data.([]float32), tensorFloat32.Shape, "")
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	if !bytes.Equal(tensorFloat32.Bytes, b.Bytes()) {
		t.Logf("Expected:\n%s", hex.Dump(tensorFloat32.Bytes))
		t.Logf("Got:\n%s", hex.Dump(b.Bytes()))
		t.Error("byte mismatch")
	}
}

